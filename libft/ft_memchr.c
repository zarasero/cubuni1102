#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = (unsigned char *)s;
	while (i < n)
	{
		if ((unsigned char)c == p[i])
			return (&p[i]);
		i++;
	}
	return (NULL);
}

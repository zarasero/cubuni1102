#include "libft.h"

long int	ft_atol(const char *nptr)
{
	long int	i;
	int			sign;
	long int	res;

	i = 0;
	sign = 1;
	res = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = (10 * res) + nptr[i] - '0';
		i++;
	}
	return (res * sign);
}
